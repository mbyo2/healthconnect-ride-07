-- Fix search_path for trigger functions to prevent security vulnerabilities

-- Update fraud_alerts trigger function
CREATE OR REPLACE FUNCTION update_fraud_alerts_updated_at()
RETURNS TRIGGER
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
BEGIN
  NEW.updated_at = now();
  RETURN NEW;
END;
$$;

